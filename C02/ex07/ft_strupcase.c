char	*ft_strupcase(char *str)
{
	int	idx;

	idx = 0;
	while (str[idx] != '\0')
	{
		if ('a' <= str[idx] && str[idx] <= 'z')
			str[idx] -= 'a' - 'A';
		idx++;
	}
	return (str);
}
